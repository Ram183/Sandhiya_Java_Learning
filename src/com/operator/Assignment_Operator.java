package com.operator;

public class Assignment_Operator {
	
	
	public static void main(String[] args) {
/*		
		Assignment_Operator
		= 
		+= 
		-= 
		*= 
		/= 
		%= 
		&= 
		^= 
		|= 
		<<= 
		>>= 
		
*/
		int a=10;  
		a+=3;
		System.out.println(a);  
		a-=4;  
		System.out.println(a);  
		a*=2;
		System.out.println(a);  
		a/=2; 
		System.out.println(a);  
		
	}

}
